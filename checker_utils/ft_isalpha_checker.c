#include "../push_swap.h"

int	ft_isalpha(char *chr)
{
	int i=0;
	while (chr[i])
	{
	if ((chr[i] >= 65 && chr[i] <= 90) || (chr[i] >= 97 && chr[i] <= 122))
	{
		return (1);
	}
	i++;
	}
	return (0);
}
